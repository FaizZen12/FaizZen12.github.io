export const TOPIC_CHIPS = Object.keys(BOOK_RECOMMENDATIONS);

// Get total number of books
export const getTotalBookCount = (): number => {
  return Object.values(BOOK_RECOMMENDATIONS).reduce((total, books) => total + books.length, 0);
};

// Get random book from category
export const getRandomBookFromCategory = (category: string): string => {
  const books = BOOK_RECOMMENDATIONS[category];
  if (!books || books.length === 0) return '';
  return books[Math.floor(Math.random() * books.length)];
};

// Search books across all categories
export const searchBooks = (query: string): { category: string; book: string }[] => {
  const results: { category: string; book: string }[] = [];
  const lowerQuery = query.toLowerCase();
  
  Object.entries(BOOK_RECOMMENDATIONS).forEach(([category, books]) => {
    books.forEach(book => {
      if (book.toLowerCase().includes(lowerQuery)) {
        results.push({ category, book });
      }
    });
  });
  
  return results;
};